// multi_horizon_bench.cpp — P2-S1-5: the multi-horizon driver compute profile.
//
// Micro-benchmarks the S1 capstone driver (risk::MultiHorizonOptimizer) on two paths:
//
//   * BM_MultiHorizonMinimal  — the MINIMAL-constraint DISPATCH path (GrossNet +
//     PositionCap only ⇒ the as-built PortfolioOptimizer inner solve, NO ADMM). This is
//     the R7 reduction path and the cheap per-rebalance cost.
//   * BM_MultiHorizonAugmented — the AUGMENTED path (FactorExposure + BetaNeutral added
//     ⇒ the S1-2 ConstrainedQpSolver ADMM). The expensive constrained book.
//
// Both sweep a small grid (N ∈ {200,500,1000}, K ∈ {5,10}, H ∈ {1,3}); N is kept modest
// so the suite finishes in seconds even in Debug. rebalances/sec is reported via
// SetItemsProcessed (one rebalance == one run() over a single-period schedule). A third
// bench (BM_MultiHorizonAdmmSolve) isolates the time-per-ADMM-solve at the largest N.
//
// DESIGN GUARANTEE reported as counters: the QP consumes V MATRIX-FREE via
// FactorModel::apply (V = X F Xᵀ + D is kept FACTORED, NEVER densified), so the per-solve
// footprint is O(N·K) not O(N²). The observed wall-clock vs N (the {200,500,1000} sweep)
// is the empirical witness; `nk` and `n2` counters print the O(N·K) and O(N²) work units
// so the gap is visible in the table.
//
// stacked_mpc is N/A here: the shipped path is the GP aim-collapse (a true stacked
// multi-period MPC QP is a recorded atx-core lift; cfg.stacked_mpc ⇒ Err(NotImplemented)).
//
// Build is Debug / clang-cl (the project default), so EVERY number here is an UPPER-BOUND
// latency — NOT a release figure. Mirrors the established risk_covariance_bench.cpp /
// cost_bench.cpp pattern (no BENCHMARK_MAIN here — it lives in bench_main.cpp). Synthetic
// data is generated by a seeded Xoshiro256pp (bench scaffold only; the driver is RNG-free).

