// ---------------------------------------------------------------------------
//  Synthetic fixture: an N-instrument, K-factor FactorModel + two distinct-horizon
//  alpha sources. Seeded so the per-instrument spread varies; F = I (SPD), D > 0.
// ---------------------------------------------------------------------------
[[nodiscard]] FactorModel make_model(usize n, usize k) {
  Xoshiro256pp rng{0x5151ULL};
  MatX x(static_cast<Eigen::Index>(n), static_cast<Eigen::Index>(k));
  for (usize i = 0; i < n; ++i) {
    for (usize c = 0; c < k; ++c) {
      x(static_cast<Eigen::Index>(i), static_cast<Eigen::Index>(c)) = 0.1 * rng.normal();
    }
  }
  MatX f = MatX::Identity(static_cast<Eigen::Index>(k), static_cast<Eigen::Index>(k));
  VecX d = VecX::Constant(static_cast<Eigen::Index>(n), 0.2);
  auto m = FactorModel::create(std::move(x), std::move(f), std::move(d), 0U, 1U);
  return std::move(*m); // bench fixture — inputs are valid by construction
}

// Two dollar-neutral-able alpha cross-sections, one per source. Seeded, deterministic.
[[nodiscard]] std::vector<f64> make_alpha(usize n, atx::u64 seed) {
  Xoshiro256pp rng{seed};
  std::vector<f64> a(n);
  for (usize i = 0; i < n; ++i) {
    a[i] = rng.normal();
  }
  return a;
}

// A two-source pack with DISTINCT horizons (a slow/persistent + a fast/decaying signal)
// — the real multi-horizon superposition. The spans must outlive run(); the caller owns
// the backing vectors and passes them in.
[[nodiscard]] HorizonSources two_source_pack(std::span<const f64> slow_src,
                                             std::span<const f64> fast_src) {
  HorizonSources hs;
  hs.pairs.emplace_back(slow_src, SignalHorizon{40.0}); // slow decay
  hs.pairs.emplace_back(fast_src, SignalHorizon{1.5});  // fast decay
  return hs;
}

// Minimal constraint set (GrossNet + PositionCap) ⇒ the dispatch path (no ADMM).
[[nodiscard]] MultiHorizonConfig minimal_cfg(usize horizon) {
  MultiHorizonConfig cfg;
  cfg.risk_aversion = 1.0;
  cfg.constraints.gross.gross_leverage = 1.0;
  cfg.constraints.gross.dollar_neutral = true;
  cfg.constraints.pos = PositionCap{1.0};
  cfg.horizon = horizon;
  cfg.trade_rate = 1.0;
  cfg.prox_max_iters = 64U;
  cfg.capacity_bound_gross = true;
  return cfg;
}

// Augmented constraint set (adds FactorExposure on factors 0/1 + BetaNeutral) ⇒ the
// S1-2 ADMM QP path. iters kept modest so the grid finishes in seconds in Debug (a
// feasibility-clearing budget is the R3 test's concern, not the bench's).
[[nodiscard]] MultiHorizonConfig augmented_cfg(usize horizon, std::span<const f64> beta) {
  MultiHorizonConfig cfg = minimal_cfg(horizon);
  cfg.risk_aversion = 0.5;
  cfg.constraints.pos = PositionCap{0.4};
  cfg.constraints.fexp = FactorExposure{{0U, 1U}, {0.2, 0.2}};
  cfg.constraints.beta = BetaNeutral{beta, 0.1};
  cfg.qp.iters = 200U; // bench-only budget (NOT a feasibility-clearing value)
  return cfg;
}

// Report the O(N·K) (matrix-free) vs O(N²) (dense-V) work units as counters so the
// design guarantee (per-solve footprint O(N·K), never O(N²)) is visible per row.
void tag_complexity(benchmark::State &state, usize n, usize k) {
  state.counters["nk"] = static_cast<f64>(n * k);
  state.counters["n2"] = static_cast<f64>(n * n);
  state.SetItemsProcessed(state.iterations()); // one rebalance per iteration ⇒ rebalances/sec
}

// ---------------------------------------------------------------------------
//  BM_MultiHorizonMinimal — the dispatch (PortfolioOptimizer) path. range: (N, K, H).
// ---------------------------------------------------------------------------
void BM_MultiHorizonMinimal(benchmark::State &state) {
  const usize n = static_cast<usize>(state.range(0));
  const usize k = static_cast<usize>(state.range(1));
  const usize h = static_cast<usize>(state.range(2));
  const FactorModel v = make_model(n, k);
  const std::vector<f64> slow = make_alpha(n, 0x1111ULL);
  const std::vector<f64> fast = make_alpha(n, 0x2222ULL);
  const RebalanceSchedule sched{{0U}};
  const CostInputs cost{0.0, 0.0, 1e9};
  const MultiHorizonOptimizer mh{minimal_cfg(h)};

  for (auto _ : state) {
    auto r = mh.run(
        sched, [&](usize) { return two_source_pack(std::span<const f64>(slow), std::span<const f64>(fast)); },
        [&](usize) -> const FactorModel & { return v; }, cost);
    benchmark::DoNotOptimize(r);
    benchmark::ClobberMemory();
  }
  tag_complexity(state, n, k);
}
BENCHMARK(BM_MultiHorizonMinimal)
    ->Args({200, 5, 1})
    ->Args({200, 5, 3})
    ->Args({500, 10, 1})
    ->Args({500, 10, 3})
    ->Args({1000, 10, 1})
    ->Args({1000, 10, 3})
    ->Unit(benchmark::kMicrosecond);

