// ---------------------------------------------------------------------------
//  BM_MultiHorizonAdmmSolve — time per ADMM solve at the LARGEST N (the augmented
//  path isolated at N=1000, K=10, H=3). One run() == one ADMM solve over a single
//  period, so the per-iteration wall time IS the per-ADMM-solve time at this N.
// ---------------------------------------------------------------------------
void BM_MultiHorizonAdmmSolve(benchmark::State &state) {
  const usize n = 1000U;
  const usize k = 10U;
  const usize h = 3U;
  const FactorModel v = make_model(n, k);
  const std::vector<f64> slow = make_alpha(n, 0x1111ULL);
  const std::vector<f64> fast = make_alpha(n, 0x2222ULL);
  const std::vector<f64> beta(n, 1.0);
  const RebalanceSchedule sched{{0U}};
  const CostInputs cost{0.0, 0.0, 1e9};
  const MultiHorizonOptimizer mh{augmented_cfg(h, std::span<const f64>(beta))};

  for (auto _ : state) {
    auto r = mh.run(
        sched, [&](usize) { return two_source_pack(std::span<const f64>(slow), std::span<const f64>(fast)); },
        [&](usize) -> const FactorModel & { return v; }, cost);
    benchmark::DoNotOptimize(r);
    benchmark::ClobberMemory();
  }
  // stacked_mpc is N/A (recorded lift): the shipped path is the GP aim-collapse. Encoded
  // as a counter so it is visible in the table next to the per-solve time.
  state.counters["stacked_mpc_NA_recorded_lift"] = 1.0;
  tag_complexity(state, n, k);
}
BENCHMARK(BM_MultiHorizonAdmmSolve)->Unit(benchmark::kMillisecond);

