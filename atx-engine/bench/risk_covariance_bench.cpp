constexpr f64 kNaN = std::numeric_limits<f64>::quiet_NaN();

// ===========================================================================
//  PanelFixture — owns a PanelView's backing storage (row 0 = newest).
// ===========================================================================
class PanelFixture {
public:
  PanelFixture(usize n_rows, usize n_inst, const std::vector<std::vector<f64>> &close)
      : n_rows_{n_rows}, n_inst_{n_inst}, cap_{pow2_ceil(n_rows)},
        mask_words_{(n_inst + 63U) / 64U} {
    universe_.reserve(n_inst);
    for (usize i = 0; i < n_inst; ++i) {
      universe_.push_back(Symbol{static_cast<u32>(i + 1U)});
    }
    fields_.assign(kPanelFieldCount * cap_ * n_inst_, kNaN);
    mask_.assign(cap_ * mask_words_, 0ULL);
    for (usize r = 0; r < n_rows_; ++r) {
      const usize phys = (n_rows_ - 1U) - r;
      for (usize i = 0; i < n_inst_; ++i) {
        const f64 c = close[r][i];
        set(PanelField::Open, phys, i, c);
        set(PanelField::High, phys, i, c);
        set(PanelField::Low, phys, i, c);
        set(PanelField::Close, phys, i, c);
        set(PanelField::Volume, phys, i, 1000.0);
        if (!std::isnan(c)) {
          mask_[phys * mask_words_ + (i >> 6U)] |= (1ULL << (i & 63U));
        }
      }
    }
  }

  [[nodiscard]] PanelView view() const noexcept {
    return PanelView{fields_.data(), mask_.data(), std::span<const InstrumentId>{universe_},
                     cap_,           head_(),      n_rows_,
                     mask_words_};
  }

private:
  [[nodiscard]] usize head_() const noexcept { return (n_rows_ == 0U) ? 0U : n_rows_ - 1U; }
  static usize pow2_ceil(usize n) noexcept {
    usize p = 1U;
    while (p < n) {
      p <<= 1U;
    }
    return p;
  }
  void set(PanelField f, usize phys, usize inst_i, f64 v) noexcept {
    const usize block = static_cast<usize>(f) * cap_ * n_inst_;
    fields_[block + phys * n_inst_ + inst_i] = v;
  }
  usize n_rows_;
  usize n_inst_;
  usize cap_;
  usize mask_words_;
  std::vector<InstrumentId> universe_;
  std::vector<f64> fields_;
  std::vector<u64> mask_;
};

// Synthetic close grid: N instruments over n_rows dates (row 0 newest), driven by a
// seeded RNG so per-date returns vary across instruments. Geometric so closes stay
// positive (step_return needs close > 0).
[[nodiscard]] std::vector<std::vector<f64>> make_close(usize n_rows, usize n_inst) {
  Xoshiro256pp rng{0x5151ULL};
  std::vector<std::vector<f64>> close(n_rows, std::vector<f64>(n_inst));
  for (usize i = 0; i < n_inst; ++i) {
    f64 px = 50.0 + 0.05 * static_cast<f64>(i);
    for (usize r = n_rows; r-- > 0U;) { // oldest -> newest
      px *= 1.0 + 0.01 * rng.normal();
      close[r][i] = px;
    }
  }
  return close;
}

// Group ids: spread the universe over `n_sectors` sectors (K == n_sectors columns).
[[nodiscard]] std::vector<u32> make_groups(usize n_inst, usize n_sectors) {
  std::vector<u32> g(n_inst);
  for (usize i = 0; i < n_inst; ++i) {
    g[i] = static_cast<u32>(i % n_sectors) + 1U;
  }
  return g;
}

// The full S8 feature set (sectors-only K, no style lookback). Every build-path flag
// engaged: robust regression, EWMA+NW factor cov, short/long blend, seeded
// eigen-adjust, VRA, EWMA+NW structural specific risk.
[[nodiscard]] FactorModelConfig full_s8_cfg() {
  FactorModelConfig cfg;
  cfg.sector_factors = true;
  cfg.style_mask = 0x00; // sectors only -> no per-instrument lookback
  cfg.cov.robust_regression = true;
  cfg.cov.robust_iters = 3;
  cfg.cov.factor_cov_method = FactorCovMethod::EwmaNeweyWest;
  cfg.cov.vol_halflife = 42U;
  cfg.cov.corr_halflife = 84U;
  cfg.cov.nw_lags = 2U;
  cfg.cov.horizon_blend = true;
  cfg.cov.horizon_blend_weight = 0.5;
  cfg.cov.vol_halflife_long = 126U;
  cfg.cov.corr_halflife_long = 252U;
  cfg.cov.eigen_adjust_sims = 256U;
  cfg.cov.eigen_adjust_seed = 0xBEEFULL;
  cfg.cov.vra_halflife = 42U;
  cfg.cov.specific_method = SpecificRiskMethod::EwmaNeweyWestStructural;
  cfg.cov.spec_halflife = 42U;
  cfg.cov.spec_nw_lags = 2U;
  cfg.cov.structural_blend = true;
  cfg.cov.spec_halflife_long = 126U;
  return cfg;
}

// A synthetic T×K factor-return series (row 0 newest) with a deliberately
// ill-conditioned structure (mirrors risk_cov_ewma_test's conditioning fixture
// mechanism): the OLD half of the window (high row indices) is a near-rank-1 STALE
// regime — every factor tracks one shared driver with only a tiny idiosyncratic
// spread, so the equal-weighted single-window MLE has a tiny smallest eigenvalue
// (large condition number). The RECENT half (low row indices) is well-spread and
// independent. The EWMA split half-lives DECAY the stale collinear block out and
// anchor the correlation on the recent rows, lifting the smallest eigenvalue. Seeded;
// deterministic.
[[nodiscard]] MatX make_factor_series(usize t, usize k) {
  Xoshiro256pp rng{0x1234ULL};
  MatX f(static_cast<Eigen::Index>(t), static_cast<Eigen::Index>(k));
  const usize half = t / 2U;
  for (usize r = 0; r < t; ++r) {
    if (r < half) {
      // Recent regime: independent, comparable scale across factors (well-spread).
      for (usize c = 0; c < k; ++c) {
        f(static_cast<Eigen::Index>(r), static_cast<Eigen::Index>(c)) = 0.012 * rng.normal();
      }
    } else {
      // Stale regime: one shared driver + a tiny idiosyncratic spread ⇒ near-rank-1.
      const f64 shared = 0.012 * rng.normal();
      for (usize c = 0; c < k; ++c) {
        f(static_cast<Eigen::Index>(r), static_cast<Eigen::Index>(c)) =
            shared + 0.0004 * rng.normal();
      }
    }
  }
  return f;
}

// Condition number (max/min eigenvalue) of a symmetric PSD matrix. +inf if the
// smallest eigenvalue is non-positive or the eigensolver fails.
[[nodiscard]] f64 cond2(const MatX &a) {
  const auto eig = atx::core::linalg::symmetric_eig(a);
  if (!eig) {
    return std::numeric_limits<f64>::infinity();
  }
  const VecX &ev = eig->values; // ascending
  const f64 lo = ev[0];
  const f64 hi = ev[ev.size() - 1];
  return (lo <= 0.0) ? std::numeric_limits<f64>::infinity() : hi / lo;
}

