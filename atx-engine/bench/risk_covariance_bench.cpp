// Full-S8 FactorModel build wall-time over an N-instrument, T-date universe.
void BM_FactorCovBuild(benchmark::State &state) {
  const usize n_inst = static_cast<usize>(state.range(0));
  const usize window = static_cast<usize>(state.range(1));
  const usize n_rows = window + 2U;
  const usize n_sectors = 8U; // K = 8 factors
  const std::vector<std::vector<f64>> close = make_close(n_rows, n_inst);
  const PanelFixture fx{n_rows, n_inst, close};
  const std::vector<u32> group = make_groups(n_inst, n_sectors);
  const FactorModelBuilder builder{full_s8_cfg()};
  const std::span<const f64> no_cap{};
  const std::span<const u32> grp{group};

  const std::vector<f64> probe(n_inst, 0.01);
  for (auto _ : state) {
    auto m = builder.build(fx.view(), window, no_cap, grp);
    f64 sink = m ? m->risk(std::span<const f64>{probe}) : 0.0;
    benchmark::DoNotOptimize(sink);
    benchmark::ClobberMemory();
  }
  state.SetItemsProcessed(state.iterations() * state.range(0));
}
// N ≈ 500 / window ≈ 250 (production target 3k–5k); a smaller N=200 point for scaling.
BENCHMARK(BM_FactorCovBuild)->Args({200, 250})->Args({500, 250})->Unit(benchmark::kMillisecond);

// Condition-number compute profile (research §10): the P4 single-MLE F vs the S8
// EWMA short/long-horizon BLEND vs the full S8 path (blend + seeded eigen-adjust), on
// the SAME synthetic factor-return series. Reports all three as counters.
//
//   * cond_p4    — raw single-window MLE covariance (LW intensity 0 ⇒ S itself).
//   * cond_blend — the EWMA split half-life short/long blend: this is the CONDITIONING
//                  cleaning the §10 deliverable targets (decays the stale collinear
//                  block out) and is expected BELOW cond_p4 on the ill-conditioned
//                  fixture (better-conditioned cleaned F).
//   * cond_s8    — blend + Monte-Carlo eigenfactor de-biasing (eigen_adjust). NOTE:
//                  eigen_adjust is a STATISTICAL de-biasing step (it inflates the
//                  under-forecast SMALL eigenvariances for forecast ACCURACY), not a
//                  conditioning step — it can legitimately shift cond either way. We
//                  report it so the full-path profile is honest; the conditioning
//                  improvement claim is carried by cond_blend vs cond_p4.
void BM_FactorCovCondition(benchmark::State &state) {
  const usize t = static_cast<usize>(state.range(0));
  const usize k = 8U;
  const MatX fseries = make_factor_series(t, k);

  // P4 path: the raw single-window MLE covariance (LW intensity 0 ⇒ S itself).
  const MatX f_p4 = atx::engine::risk::detail::factor_covariance(fseries, /*cfg_shrink=*/0.0);
  // S8 EWMA short/long-horizon blend (the conditioning cleaning).
  const MatX f_short = ewma_factor_covariance(fseries, /*vol_hl=*/4U, /*corr_hl=*/6U, /*L=*/0U);
  const MatX f_long = ewma_factor_covariance(fseries, /*vol_hl=*/8U, /*corr_hl=*/12U, /*L=*/0U);
  const MatX f_blend = atx::engine::risk::blend_factor_cov(f_short, f_long, 0.5);
  // Full S8 path: the blend, then seeded Monte-Carlo eigenfactor de-biasing.
  const auto adj = atx::engine::risk::eigen_adjust(f_blend, /*sims=*/256U, /*amplify=*/1.0,
                                                   /*seed=*/0xBEEFULL);
  const MatX f_s8 = adj ? *adj : f_blend;

  const f64 cond_p4 = cond2(f_p4);
  const f64 cond_blend = cond2(f_blend);
  const f64 cond_s8 = cond2(f_s8);
  for (auto _ : state) {
    f64 sink = cond_p4 + cond_blend + cond_s8; // mutable lvalue -> non-deprecated DoNotOptimize
    benchmark::DoNotOptimize(sink);
    benchmark::ClobberMemory();
  }
  state.counters["cond_p4"] = cond_p4;
  state.counters["cond_blend"] = cond_blend;
  state.counters["cond_s8"] = cond_s8;
}
BENCHMARK(BM_FactorCovCondition)->Arg(250);

